package Oficina;

public class Carta {
	
	private String nombre;
	private String apellido;
	private String destinatario;
	private String ciudad;
	private String pais;
	
	
	public Carta(String nombre, String apellido, String destinatario, String ciudad, String pais) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.destinatario = destinatario;
		this.ciudad = ciudad;
		this.pais = pais;
	}
	
	

}
