package ficheros;

public class nombre {

	private String nombre;
	private String Apellido;
	private String Mail;

	public nombre(String nombre, String apellido, String mail) {
		super();
		this.nombre = nombre;
		this. Apellido = apellido;
		this. Mail = mail;
	}

	public String getNombre() {
		return nombre;
	}

	public String getApellido() {
		return Apellido;
	}

	public String getMail() {
		return Mail;
	}
	
	
	
}
